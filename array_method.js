// CLASS TOPICS: FEW METHOD OF Array Data Types

// reverse
list = [1,2,3,4,5,6].reverse();
list2 = ["a", "b", "c", "d"].reverse();
console.log(list)
console.log(list2)

// Pop method remove last element
list3 = [1,2,3,4,5,6].pop(); // print and return last element  
list4 = [1,2,3,4,5,6]; //// return new array after remove last element
list4.pop();
console.log(list4)
console.log(list3)

// slice method
list5 = [1,2,3,4,5,6].slice(0,4)
console.log(list5)

// tostring method
list6 = [1,2,3,4,5,6].toString()
console.log(list6)

// Concat method
list7 = [1,2,3,4,5,6].concat(["a", "b"], ["c", "d"])
console.log(list7)

