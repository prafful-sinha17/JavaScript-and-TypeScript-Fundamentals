// Method of String


// method 1
myVal = "hello".toUpperCase();
myVa2 = "hello".toLowerCase();
console.log(myVal)
console.log(myVa2)

// method 2
myVa3 = "hello world";
myNewValue = myVa3.toUpperCase()
console.log(myNewValue)


// replace 
myVa4 = "hello".replace('h', 'b')
console.log(myVa4)

// split method
myVa4 = "hello".split('e')
console.log(myVa4)

// substring method
myVa5 = "hello".substr(2)
myVa6 = "hello".substr(0,2)
console.log(myVa5)
console.log(myVa6)

// trim method
myVa7 = "   hello    ".trim()
console.log(myVa7)


// slice method // (initial element, last element)
myVa8 = "Hello World".slice(0,5)
console.log(myVa8)